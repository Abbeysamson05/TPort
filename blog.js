function blogCard() {
  const container = document.getElementById("blogContainer");

  cardsData.forEach((card) => {
    const cardContainer = document.createElement("div");
    const miniContainer = document.createElement("div");
    cardContainer.classList.add("blog-container");

    const linkElement = document.createElement("a");
    linkElement.classList.add("card-element");
    linkElement.href = card.url;

    const imageElement = document.createElement("img");
    imageElement.classList.add("blog-image");
    imageElement.src = card.imageUrl;
    imageElement.alt = "Card Image";
    const cardElement = document.createElement("div");

    const titleElement = document.createElement("h3");
    titleElement.classList.add("title", "fw-bold", "my-4");
    titleElement.textContent = card.title;

    const descriptionElement = document.createElement("p");
    descriptionElement.classList.add("description");
    descriptionElement.textContent = card.description;

    linkElement.appendChild(imageElement);
    cardElement.appendChild(linkElement);
    cardElement.appendChild(titleElement);
    cardElement.appendChild(descriptionElement);

    cardElement.appendChild(miniContainer);
    cardContainer.appendChild(cardElement);

    const linkList = document.createElement("ol");
    card.innerUrl.forEach((innerLink) => {
      const listItem = document.createElement("li");

      const innerLinkElement = document.createElement("a");
      innerLinkElement.classList.add("innerLink");
      innerLinkElement.href = innerLink.url;
      innerLinkElement.textContent = innerLink.linkheader;

      listItem.appendChild(innerLinkElement);
      linkList.appendChild(listItem);
    });

    cardContainer.appendChild(linkList);

    container.appendChild(cardContainer);
  });
}
blogCard();
